// Setup type definitions for built-in Supabase Runtime APIs
import "jsr:@supabase/functions-js/edge-runtime.d.ts"

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type"
};

Deno.serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === "OPTIONS") {
    return new Response(null, {
      headers: corsHeaders
    });
  }

  const { input, country } = await req.json()

  const GOOGLE_PLACES_API_KEY = Deno.env.get("GOOGLE_PLACES_API_KEY");

  const response = await fetch("https://places.googleapis.com/v1/places:autocomplete", {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      "X-Goog-Api-Key": GOOGLE_PLACES_API_KEY,
    },
    body: JSON.stringify({
      input: input,
      regionCode: country,
    }),
  });

  const data = await response.json()
  const formattedSuggestions = data.suggestions?.map((sugg: any) => ({
    description: sugg.placePrediction.text?.text || "Unknown Address",
    placeId: sugg.placePrediction.placeId
  })) || [];

  return new Response(JSON.stringify(formattedSuggestions), { headers: { ...corsHeaders, "Content-Type": "application/json" } })
})

